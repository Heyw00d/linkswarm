#!/usr/bin/env node
/**
 * LinkSwarm llms.txt Generator
 * 
 * Crawls a website and generates an llms.txt file using MarkItDown + LLM
 * 
 * Usage: node generate-llms-txt.mjs <domain> [--output llms.txt]
 */

import { execSync } from 'child_process';
import fs from 'fs/promises';
import path from 'path';

const MARKITDOWN_CMD = `source ~/clawd/tools/markitdown/.venv/bin/activate && markitdown`;

// Pages to crawl for llms.txt content
const KEY_PAGES = [
  '/',
  '/about',
  '/docs',
  '/api',
  '/pricing',
  '/features',
  '/faq',
  '/contact'
];

async function fetchAndConvert(url) {
  try {
    const result = execSync(`bash -c '${MARKITDOWN_CMD} "${url}"'`, {
      maxBuffer: 10 * 1024 * 1024,
      timeout: 30000,
      encoding: 'utf-8'
    });
    return result;
  } catch (err) {
    console.warn(`  ⚠ Failed to fetch ${url}`);
    return null;
  }
}

async function generateLlmsTxt(domain, contents) {
  // Build prompt for LLM
  const prompt = `You are generating an llms.txt file for ${domain}.

llms.txt is a markdown file that helps LLMs understand a website. It should include:
- A title (# SiteName)
- A one-line description (> blockquote)
- What the site/product does
- Key features
- How to use it / API if applicable
- Links to important pages
- Contact/support info

Here is the content from the website:

${contents}

Generate a clean, concise llms.txt file following the llmstxt.org standard. Keep it under 2000 words. Focus on what an LLM or AI agent would need to know to understand and interact with this site.`;

  // Call Claude via Clawdbot's API or direct
  try {
    const result = execSync(`echo '${prompt.replace(/'/g, "'\\''")}' | claude --print`, {
      maxBuffer: 10 * 1024 * 1024,
      timeout: 60000,
      encoding: 'utf-8'
    });
    return result;
  } catch {
    // Fallback: return template
    return `# ${domain}

> [Auto-generated llms.txt - needs review]

## About

Content extracted from ${domain}. Please review and customize.

## Content

${contents.substring(0, 3000)}

---
Generated by LinkSwarm llms.txt Generator
`;
  }
}

async function main() {
  const args = process.argv.slice(2);
  
  if (args.length === 0) {
    console.log(`
LinkSwarm llms.txt Generator

Usage: node generate-llms-txt.mjs <domain> [options]

Options:
  --output, -o <file>   Output file (default: stdout)
  --pages <urls>        Comma-separated list of pages to crawl
  --help                Show this help

Examples:
  node generate-llms-txt.mjs example.com
  node generate-llms-txt.mjs example.com -o llms.txt
  node generate-llms-txt.mjs example.com --pages /,/about,/api
`);
    process.exit(0);
  }

  let domain = args[0];
  let outputFile = null;
  let customPages = null;

  // Parse args
  for (let i = 1; i < args.length; i++) {
    if (args[i] === '-o' || args[i] === '--output') {
      outputFile = args[++i];
    } else if (args[i] === '--pages') {
      customPages = args[++i].split(',');
    }
  }

  // Normalize domain
  if (!domain.startsWith('http')) {
    domain = `https://${domain}`;
  }
  const baseUrl = new URL(domain);
  
  console.log(`🔍 Generating llms.txt for ${baseUrl.hostname}...\n`);

  // Crawl pages
  const pages = customPages || KEY_PAGES;
  const contents = [];
  
  for (const page of pages) {
    const url = new URL(page, baseUrl).href;
    console.log(`  📄 Fetching ${url}...`);
    const content = await fetchAndConvert(url);
    if (content) {
      contents.push(`## ${page}\n\n${content}\n`);
    }
  }

  if (contents.length === 0) {
    console.error('❌ No content could be extracted');
    process.exit(1);
  }

  console.log(`\n✨ Extracted ${contents.length} pages, generating llms.txt...\n`);

  // Generate llms.txt
  const combinedContent = contents.join('\n---\n\n');
  const llmsTxt = await generateLlmsTxt(baseUrl.hostname, combinedContent);

  // Output
  if (outputFile) {
    await fs.writeFile(outputFile, llmsTxt);
    console.log(`✅ Saved to ${outputFile}`);
  } else {
    console.log('--- llms.txt ---\n');
    console.log(llmsTxt);
  }
}

main().catch(console.error);
